void SetBufText(Glib::RefPtr<Gtk::TextBuffer> tv_buffer,Glib::ustring mail_text)
{
	Gtk::TextIter start = tv_buffer->get_iter_at_offset(0);
	Gtk::TextIter end = tv_buffer->get_iter_at_offset(tv_buffer->get_text().length());
    tv_buffer->remove_all_tags(start,end);
	tv_buffer->set_text(mail_text);
	Glib::ustring ustr = tv_buffer->get_text()+" ";
	int pos = 0;
	while((pos = ustr.find("http",pos))>=0)
	{
        if(ustr[pos+4]==':' || (ustr[pos+4]=='s' && ustr[pos+5]==':'))
		{
			int t = ustr.find_first_of(" \n\t\r",pos);
			if(t>=0)
			{
				start = tv_buffer->get_iter_at_offset(pos);
				Gtk::TextIter end = tv_buffer->get_iter_at_offset(t);
				tv_buffer->apply_tag_by_name("link",start,end);
				pos = t;
			}
			else pos += 4;
		}
		else pos += 4;
	}
}

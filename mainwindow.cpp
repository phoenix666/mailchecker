char* my_encrypt(char *str,char *key)
{
  // returns non-"zero safe" string with actual length in first 2 bytes 
  int len = strlen(str)+1;
  int n = floor(sqrt(len))+1;
  char *res = new char[n*n+4];
  int lk = strlen(key);
  int k = 0;
  res += 2;
  for(int i = 0;i<n;i++)
  {
    for(int j = 0;j<n;j++)
    {
      int t = j*n+i;
	  char tt = ' ';
      if(t<len) tt = str[t];
      res[k++] = tt^key[t%lk];
    }
  }
  //res[k] = 0;
  res -= 2;
  *(short int*)res = k;
  return(res);
}
char* my_decrypt(char *str,char *key)
{
  // str should contain length in first two bytes
  // returns normal 0-terminated string
  int len = *(short int*)str;
  str += 2;
  int n = sqrt(len);
  char *res = new char[len+1];
  int lk = strlen(key);
  int k = 0;
  for(int i = 0;i<n;i++)
  {
    for(int j = 0;j<n;j++)
    {
      res[k++] = str[j*n+i]^key[(i*n+j)%lk];
    }
  }
  res[k] = 0;
  return(res);
}


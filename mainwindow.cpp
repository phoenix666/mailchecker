void MainWindow::save_data()
{
	// data scrambled with a very primitive cypher, just so that a person who
	// takes a peek on the file can not read the password right away
	// otherwise it's not even a slightly proof to decoding
	Glib::ustring value = "";
	value += "<conn_data>";
	value += eServer.get_buffer()->get_text()+"\n";
	value += ePort.get_buffer()->get_text()+"\n";
	value += eUsername.get_buffer()->get_text()+"\n";
	value += ePassword.get_buffer()->get_text()+"\n";
	value += "</conn_data>\n";
	value += "<aliases>"+alBuffer->get_text()+"</aliases>\n";
	value += "<black_list>"+blBuffer->get_text()+"</black_list>\n";
	value += "<white_list>"+wlBuffer->get_text()+"</white_list>\n";
	int need_to_save = 1;
	int len = strlen(value.c_str());
	char *t = new char[len+1];
	strcpy(t,value.c_str());
	int i = 0,j = 0;
	while(i<len-1)
	{
		if(t[i]=='\n' && t[i+1]=='\n')
		{
			i++;
		}
		else 
		{
			if(i!=j) t[j] = t[i];
			i++;
			j++;
		}
	}
	if(i!=j) t[j] = 0;
	if(old_data!=NULL)
	{
		if(strcmp(old_data,t)==0) need_to_save = 0;
	}
	if(need_to_save)
	{
		if(old_data!=NULL) delete(old_data);
		old_data = new char[strlen(t)+1];
		strcpy(old_data,t);
		FILE *fp = fopen("data2.dat","wb");
		if(fp)
		{
			char *cr = my_encrypt(old_data,"mdhar2540rwfd1e3f2lkb");
			int tl = *((short int*)cr)+2;
			fwrite(cr,1,tl,fp);
			fclose(fp);
			delete(cr);
		}
		//puts("saved");
	}
	//else puts("no need to save");
	delete(t);
}

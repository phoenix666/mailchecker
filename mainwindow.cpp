void MainWindow::RepopulateListBox()
{
	tv_buffer->set_text("");
	MailList.unselect_all();
	std::vector<Gtk::Widget*> children = MailList.get_children();
	for (Widget* w : children) {
		MailList.remove(*w);
		delete w;
	}
	
	if(GlobalMailBox.mCount>0)
	{
		for(int i = 0;i<GlobalMailBox.mCount;i++)
		{
			MH_Row *row = Gtk::manage(new MH_Row(&GlobalMailBox.messages[i]));
			MailList.append(*row);
		}
	}
	MailList.show_all_children();
	Gtk::ListBoxRow *t = MailList.get_row_at_index(0);
	if(t) 
	{
		MailList.select_row(*t);
		SetBufText(tv_buffer,((MH_Row*)t)->Message()->Body());
		//tv_buffer->set_text(((MH_Row*)t)->Message()->Body());
	}
}


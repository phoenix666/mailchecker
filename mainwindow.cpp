bool MainWindow::text_cursor_move(GdkEventMotion* motion_event)
{
	Gtk::TextIter iter;
	int x,y;
	MailView.window_to_buffer_coords(Gtk::TextWindowType::TEXT_WINDOW_TEXT,motion_event->x,motion_event->y,x,y);
	MailView.get_iter_at_location(iter,x,y);
	if(iter.get_tags().size()>0)
	{
		if(cursor_hand == false)
		{
			MailView.get_window(Gtk::TEXT_WINDOW_TEXT)->set_cursor(Gdk::Cursor::create(Gdk::CursorType::HAND1));
			cursor_hand = true;
		}
	}
	else if(cursor_hand == true)
	{
		MailView.get_window(Gtk::TEXT_WINDOW_TEXT)->set_cursor();
			cursor_hand = false;
	}
	return false;
}
bool MainWindow::link_clicked(const Glib::RefPtr<Glib::Object>& event_object, GdkEvent* gdk_event, const Gtk::TextIter& iter)
{
	//if(gdk_event->type==Gdk::ENTER_NOTIFY) does not receive these events
	if(gdk_event->type==Gdk::BUTTON_PRESS && gdk_event->button.button==1)
	{
		int offset = iter.get_offset();
		Glib::ustring str = tv_buffer->get_text()+" ";
		int pos = str.rfind("http",offset);
		if(pos)
		{
			int t = str.find_first_of(" \n\t\r",pos);
			if(t>=0)
			{
				//printf("%s\n",str.substr(pos,t-pos).c_str());
				char *ts = new char[500];
				strcpy(ts,"xdg-open '");
				strcat(ts,str.substr(pos,t-pos).c_str());
				strcat(ts,"'");
				system(ts);
				delete(ts);
			}
		}
		return(true);
	}
	return(false);
}
